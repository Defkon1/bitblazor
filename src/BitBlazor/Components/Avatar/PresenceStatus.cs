namespace BitBlazor.Components;

/// <summary>
/// Defines the presence status in an avatar.
/// </summary>
public enum PresenceStatus
{
    /// <summary>
    /// No presence status provided
    /// </summary>
    None,

    /// <summary>
    /// The user is active
    /// </summary>
    Active,

    /// <summary>
    /// The user is busy
    /// </summary>
    Busy,

    /// <summary>
    /// The presence status of the user is hidden or private
    /// </summary>
    Hidden
}

