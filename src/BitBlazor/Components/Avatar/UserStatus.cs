namespace BitBlazor.Components;

/// <summary>
/// Defines the user status in an avatar.
/// </summary>
public enum UserStatus
{
    /// <summary>
    /// No user status provided
    /// </summary>
    None,

    /// <summary>
    /// The user has been approved
    /// </summary>
    Approved,

    /// <summary>
    /// The user has been declined
    /// </summary>
    Declined,

    /// <summary>
    /// The user has been notified
    /// </summary>
    Notified
}

